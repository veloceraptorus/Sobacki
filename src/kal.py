сколька_у_вас_денги = int(input("введите количество ващих денег "))
денги = сколька_у_вас_денги
цена_кваса_в_рублях = 120
def пит_квас():
    пенги = str(денги-цена_кваса_в_рублях)
    return "папил квасу, осталось " + пенги + " денег"

if денги >= цена_кваса_в_рублях:
    print(пит_квас())
else:
    print("квасу бы")
